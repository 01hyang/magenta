#include <stdarg.h>
#include <stdio.h>
#include <string.h>

#include <magenta/syscalls.h>
#include <mxio/debug.h>

void cprintf(const char* fmt, ...) {
    char buf[128];
    va_list ap;
    va_start(ap, fmt);
    vsnprintf(buf, sizeof(buf), fmt, ap);
    va_end(ap);
    buf[sizeof(buf) - 1] = 0;
    _magenta_debug_write(buf, strlen(buf));
}
